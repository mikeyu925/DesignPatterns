package com.ywh.dp.chain;

public class Request {
    private String type; // 类别
    private int  money;  // 金额

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getMoney() {
        return money;
    }

    public void setMoney(int money) {
        this.money = money;
    }
}
